}

function Write-HomeRedirectPage {
    param([string]$TargetSiteDir)

    $candidates = @(
        "src\pages\index.js",
        "src\pages\index.jsx",
        "src\pages\index.ts",
        "src\pages\index.tsx",
        "src\pages\index.mdx"
    )

    foreach ($relativePath in $candidates) {
        $candidatePath = Join-Path $TargetSiteDir $relativePath
        if (Test-Path $candidatePath) {
            Remove-Item -Path $candidatePath -Force
        }
    }

    $redirectPage = @'
import React, {useEffect} from 'react';
import useBaseUrl from '@docusaurus/useBaseUrl';

export default function Home() {
  const target = useBaseUrl('/docs/intro');

  useEffect(() => {
    window.location.replace(target);
  }, [target]);

  return (
    <main style={{padding: '2rem'}}>
      <p>
        Redirecting to <a href={target}>{target}</a>...
      </p>
    </main>
  );
}
'@

    Set-Content -Path (Join-Path $TargetSiteDir "src\pages\index.js") -Value $redirectPage -NoNewline
}

$repoRoot = Split-Path -Parent $PSScriptRoot
$platformDocsDir = Join-Path $repoRoot "platform-docs"

if (-not (Test-Path $platformDocsDir)) {
    throw "platform-docs directory not found at $platformDocsDir"
}

if ([string]::IsNullOrWhiteSpace($SiteDir)) {
    $SiteDir = Join-Path $repoRoot ".tmp\platform-docs-site"
}

Assert-CommandExists -Name "node"
Assert-CommandExists -Name "npm"
Assert-CommandExists -Name "npx"

if ($Mode -eq "clean") {
    if (Test-Path $SiteDir) {
        Write-Host "Removing site directory: $SiteDir"
        Remove-Item -Path $SiteDir -Recurse -Force
    }
    else {
        Write-Host "Nothing to clean. Site directory does not exist: $SiteDir"
    }
    exit 0
}

Initialize-Site -TargetDir $SiteDir -ForceRecreate:$Recreate.IsPresent
Sync-PlatformDocs -SourceDir $platformDocsDir -TargetSiteDir $SiteDir
Write-HomeRedirectPage -TargetSiteDir $SiteDir
Write-DocusaurusConfig -TargetSiteDir $SiteDir

if (Test-Path (Join-Path $SiteDir "package-lock.json")) {
    Invoke-Step -WorkingDirectory $SiteDir -Command "npm" -Arguments @("ci")
}
else {
    Invoke-Step -WorkingDirectory $SiteDir -Command "npm" -Arguments @("install")
}

switch ($Mode) {
    "prepare" {
        Write-Host "Prepared preview site at: $SiteDir"
    }
    "build" {
        Invoke-Step -WorkingDirectory $SiteDir -Command "npm" -Arguments @("run", "build")
        Write-Host "Build complete. Output folder: $(Join-Path $SiteDir 'build')"
    }
    "serve" {
        Invoke-Step -WorkingDirectory $SiteDir -Command "npm" -Arguments @("run", "build")
        if ($OpenBrowser) {
            Start-Process "http://localhost:$Port/"
        }
        Invoke-Step -WorkingDirectory $SiteDir -Command "npm" -Arguments @("run", "serve", "--", "--host", "0.0.0.0", "--port", "$Port")
    }
    "start" {
        if ($OpenBrowser) {
            Start-Process "http://localhost:$Port/"
        }
        Invoke-Step -WorkingDirectory $SiteDir -Command "npm" -Arguments @("run", "start", "--", "--host", "0.0.0.0", "--port", "$Port")
    }
    default {
        throw "Unsupported mode: $Mode"
    }
}
