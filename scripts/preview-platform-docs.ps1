param(
    [Parameter(Mandatory = $false)]
    [ValidateSet("prepare", "start", "build", "serve", "clean")]
    [string]$Mode = "start",

    [Parameter(Mandatory = $false)]
    [string]$SiteDir,

    [Parameter(Mandatory = $false)]
    [int]$Port = 3000,

    [Parameter(Mandatory = $false)]
    [switch]$Recreate,

    [Parameter(Mandatory = $false)]
    [switch]$OpenBrowser
)

$ErrorActionPreference = "Stop"

function Assert-CommandExists {
    param([string]$Name)

    if (-not (Get-Command $Name -ErrorAction SilentlyContinue)) {
        throw "Required command not found: $Name"
    }
}

function Invoke-Step {
    param(
        [string]$WorkingDirectory,
        [string]$Command,
        [string[]]$Arguments
    )

    Push-Location $WorkingDirectory
    try {
        & $Command @Arguments
        if ($LASTEXITCODE -ne 0) {
            throw "$Command $($Arguments -join ' ') failed with exit code $LASTEXITCODE"
        }
    }
    finally {
        Pop-Location
    }
}

function Initialize-Site {
    param(
        [string]$TargetDir,
        [bool]$ForceRecreate
    )

    if ($ForceRecreate -and (Test-Path $TargetDir)) {
        Write-Host "Removing existing site scaffold at: $TargetDir"
        Remove-Item -Path $TargetDir -Recurse -Force
    }

    if (-not (Test-Path $TargetDir)) {
        $parent = Split-Path -Parent $TargetDir
        if (-not (Test-Path $parent)) {
            New-Item -Path $parent -ItemType Directory -Force | Out-Null
        }

        Write-Host "Creating Docusaurus scaffold at: $TargetDir"
        Invoke-Step -WorkingDirectory $parent -Command "npx" -Arguments @("--yes", "create-docusaurus@latest", $TargetDir, "classic", "--javascript", "--package-manager", "npm")
    }
}

function Sync-PlatformDocs {
    param(
        [string]$SourceDir,
        [string]$TargetSiteDir
    )

    $docsTarget = Join-Path $TargetSiteDir "docs"
    if (-not (Test-Path $docsTarget)) {
        New-Item -Path $docsTarget -ItemType Directory -Force | Out-Null
    }

    Get-ChildItem -Path $docsTarget -Force | Remove-Item -Recurse -Force

    $excludedNames = @(".github", "docusaurus")

    Get-ChildItem -Path $SourceDir -Force | Where-Object {
        $excludedNames -notcontains $_.Name
    } | ForEach-Object {
        Copy-Item -Path $_.FullName -Destination $docsTarget -Recurse -Force
    }

    $sidebarsSource = Join-Path $SourceDir "docusaurus\sidebars.js"
    if (-not (Test-Path $sidebarsSource)) {
        throw "Missing sidebars file at $sidebarsSource"
    }

    Copy-Item -Path $sidebarsSource -Destination (Join-Path $TargetSiteDir "sidebars.js") -Force
}

